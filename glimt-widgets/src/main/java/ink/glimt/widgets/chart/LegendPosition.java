package ink.glimt.widgets.chart;

/**
 * Position of the legend in a {@link Chart}.
 */
public enum LegendPosition {
    /** Top center */
    TOP,
    /** Top left corner */
    TOP_LEFT,
    /** Top right corner (default) */
    TOP_RIGHT,
    /** Left side, vertically centered */
    LEFT,
    /** Right side, vertically centered */
    RIGHT,
    /** Bottom center */
    BOTTOM,
    /** Bottom left corner */
    BOTTOM_LEFT,
    /** Bottom right corner */
    BOTTOM_RIGHT
}
