package ink.glimt.tui.event;

/**
 * Represents mouse buttons.
 */
public enum MouseButton {
    /** Left mouse button (button 1). */
    LEFT,

    /** Middle mouse button (button 2, scroll wheel click). */
    MIDDLE,

    /** Right mouse button (button 3). */
    RIGHT,

    /** No button (for move events). */
    NONE
}
